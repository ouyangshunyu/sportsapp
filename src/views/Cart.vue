<template>
    <div>
        <van-nav-bar>
            <van-icon name="arrow-left" slot="left" color="gray" title="购物车" @click="onClickLeft"/>
        </van-nav-bar>
        <van-checkbox-group class="card-goods" v-model="checkedGoods">
            <van-checkbox
                class="card-goods__item"
                v-for="item in goods"
                :key="item.id"
                :name="item.id"
            >
                <van-card
                    :title="item.title"
                    :desc="item.desc"
                    :num="item.num"
                    :price="formatPrice(item.price)"
                    :thumb="item.thumb"
                />
            </van-checkbox>
        </van-checkbox-group>
        <van-submit-bar
            :price="totalPrice"
            :disabled="!checkedGoods.length"
            :button-text="submitBarText"
            @submit="onSubmit"
        />
        <van-goods-action>
            <van-goods-action-mini-btn
                icon="chat-o"
                text="客服"
                @click="onClickMiniBtn"
            />
            <van-goods-action-mini-btn
                icon="cart-o"
                text="购物车"
                @click="onClickMiniBtn"
            />
            <van-goods-action-big-btn
                text="加入购物车"
                @click="onClickBigBtn"
            />
            <van-goods-action-big-btn
                primary
                text="立即购买"
                @click="onClickBigBtn"
            />
        </van-goods-action>
    </div>
</template>

<script>
    import { Checkbox, CheckboxGroup, Card, SubmitBar, Toast } from 'vant';
    export default {
        components: {
            [Card.name]: Card,
            [Checkbox.name]: Checkbox,
            [SubmitBar.name]: SubmitBar,
            [CheckboxGroup.name]: CheckboxGroup
        },
        data() {
            return {
                title:'购物车',
                Card:'',
                Checkbox:'',
                SubmitBar:'',
                CheckboxGroup:'',
                checkedGoods: ['1', '2', '3'],
                goods: [
                    {
                    id: '1',
                    title: '进口香蕉',
                    desc: '约250g，2根',
                    price: 200,
                    num: 1,
                    thumb: 'https://img.yzcdn.cn/public_files/2017/10/24/2f9a36046449dafb8608e99990b3c205.jpeg'
                },
                    {
                    id: '2',
                    title: '陕西蜜梨',
                    desc: '约600g',
                    price: 690,
                    num: 1,
                    thumb: 'https://img.yzcdn.cn/public_files/2017/10/24/f6aabd6ac5521195e01e8e89ee9fc63f.jpeg'
                },
                    {
                    id: '3',
                    title: '美国伽力果',
                    desc: '约680g/3个',
                    price: 2680,
                    num: 1,
                    thumb: 'https://img.yzcdn.cn/public_files/2017/10/24/320454216bbe9e25c7651e1fa51b31fd.jpeg'
                }
                ]
            };
        },
        computed: {
            submitBarText() {
                const count = this.checkedGoods.length;
                return '结算' + (count ? `(${count})` : '');
            },
            totalPrice() {
                return this.goods.reduce((total, item) => total + (this.checkedGoods.indexOf(item.id) !== -1 ? item.price : 0), 0);
            }
        },
        methods: {
            formatPrice(price) {
                return (price / 100).toFixed(2);
            },
            onSubmit() {
                Toast('点击结算');
            },
            onClickMiniBtn() {
                Toast('点击图标');
            },
            onClickBigBtn() {
                Toast('点击按钮');
            },
            onClickLeft(){
                this.$router.go(-1)
            }
        }
    };
</script>

